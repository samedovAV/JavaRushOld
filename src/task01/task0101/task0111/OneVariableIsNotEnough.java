package task01.task0101.task0111;

/*
* Объявить переменные a, b типа int и переменную s типа String
* Сразу же в строке объявления присвоить им любые разные значения
*/

public class OneVariableIsNotEnough {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        String s = "Hello";
        System.out.println("There are ints: " + a + " " + b);
        System.out.println("String: " + s);
    }
}
